
public class DataType {

	public static void main(String[] args) {
		// declaring variables: int,double,char,boolean are primitive datatypes
		//int
			int i = 10;
			int j = 20;
			int k = i+j;
			System.out.println("Addition of two integer numbers :" + k);
		
		//double
			double d = 12.01;
			double d1 = 11.33;
			double d2 = d + d1;
			System.out.println("addition of two double numbers : " + d2);
			
		//char
			char c = 'Z';
			
		//boolean
			boolean b = true;
			boolean b1 = false;
			
		//string------>Not a datatype but a class,but we can use it as datatype
			String s = "Hello ";
			String s1 = "Friends";
			System.out.println(s + s1);
			System.out.println("I am gonna be a developer!!!!");
			
			
			
			
			

	}


}
