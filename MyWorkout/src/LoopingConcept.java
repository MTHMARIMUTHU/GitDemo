
public class LoopingConcept {

	public static void main(String[] args) {
		
		// while loop--> disadv - infinite loop if no increment part
		
		int i = 1; // initialization
		while(i<=10) { //conditional
			System.out.println(i);
			i++; //incremental
		}
		
	//for loop
		for(i=0;i<=10;i++) {
			System.out.println(i);
		}
		System.out.println("********************");
		
		//print 10 to 1
		int j;
		for(j=10;j>=1;j--) {
			System.out.println(j);
		}
		
		
		
	
		
		
		
		
		
		

	}

}
